var quotesCopy = [...quotes]; // نسخة من المصفوفة الأصلية
var btn = document.getElementById('btnClick');
btn.onclick = disPlay;

function disPlay() {
  // إذا كانت نسخة المصفوفة فارغة، أعد ضبطها
  if (quotesCopy.length === 0) {
    quotesCopy = [...quotes];
  }

  // احصل على عنصر عشوائي من النسخة
  var randomIndex = Math.floor(Math.random() * quotesCopy.length);
  var quote = quotesCopy[randomIndex];

  // اعرض الاقتباس
  document.getElementById('quotesOutput').innerText = quote.text;
  document.getElementById('outherOutput').innerText = quote.author;

  // احذف الاقتباس من النسخة
  quotesCopy.splice(randomIndex, 1);
}




let todoList = JSON.parse(localStorage.getItem("todoList")) || []; // استرجاع المهام المحفوظة من localStorage
const addTask = document.getElementById("addTask");

// تحميل المهام المحفوظة عند فتح الصفحة
window.onload = function () {
  renderTodoList();
};

addTask.addEventListener("click", addTodoItem);

function addTodoItem() {
  let item = document.getElementById("todoInput").value.trim();
  if (!item) return; // تجاهل الإضافات الفارغة

  todoList.push(item);
  saveToLocalStorage(); // حفظ القائمة في localStorage
  renderTodoList(); // تحديث العرض

  document.getElementById("todoInput").value = ""; // تصفية حقل الإدخال
}

function renderTodoList() {
  let list = document.getElementById("todoList");
  list.innerHTML = ""; // تصفية القائمة قبل إعادة العرض

  todoList.forEach((item) => {
    let listItem = document.createElement("div");
    listItem.className = "todoItem";

    let checkbox = document.createElement("input");
    checkbox.type = "checkbox";
    checkbox.onclick = function () {
      if (checkbox.checked) {
        itemText.style.textDecoration = "line-through";
        listItem.classList.add("checked");
      } else {
        itemText.style.textDecoration = "none";
        listItem.classList.remove("checked");
      }
    };
    listItem.appendChild(checkbox);

    let itemText = document.createElement("span");
    itemText.textContent = item;
    listItem.appendChild(itemText);

    let buttonsDiv = document.createElement("div");
    listItem.appendChild(buttonsDiv);

    let editButton = document.createElement("button");
    editButton.innerHTML = '<i class="fas fa-edit"></i>';
    editButton.onclick = function () {
      itemText.contentEditable = true;
      itemText.focus();
    };
    buttonsDiv.appendChild(editButton);

    itemText.onblur = function () {
      itemText.contentEditable = false;
      let index = todoList.indexOf(item);
      if (index !== -1) {
        todoList[index] = itemText.textContent;
        saveToLocalStorage(); // تحديث التخزين بعد التعديل
      }
    };

    let deleteButton = document.createElement("button");
    deleteButton.innerHTML = '<i class="fas fa-trash"></i>';
    deleteButton.onclick = function () {
      list.removeChild(listItem);
      let index = todoList.indexOf(item);
      if (index !== -1) {
        todoList.splice(index, 1);
        saveToLocalStorage(); // تحديث التخزين بعد الحذف
      }
    };
    buttonsDiv.appendChild(deleteButton);

    list.appendChild(listItem);
  });
}

function saveToLocalStorage() {
  localStorage.setItem("todoList", JSON.stringify(todoList));
}


